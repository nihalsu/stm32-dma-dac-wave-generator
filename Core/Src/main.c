/**
  * @brief  Dizileri verilen voltaj değerine göre yeniden hesaplar.
  */
void Set_Wave_Amplitude(float target_voltage) {
    if (target_voltage > 3.5f) target_voltage = 3.5f;
    if (target_voltage < 0.0f) target_voltage = 0.0f;

    current_voltage = target_voltage; // Durumu guncelle

    float amplitude = (target_voltage / 3.5f) * 4095.0f;
    float offset = amplitude / 2.0f;

    for (int i = 0; i < SAMPLES; i++) {
        sine_val[i]   = (uint16_t)((sin(i * 2 * M_PI / SAMPLES) * offset) + offset);
        cos_val[i]    = (uint16_t)((cos(i * 2 * M_PI / SAMPLES) * offset) + offset);

        if (i < SAMPLES / 2) {
            tri_val[i] = (uint16_t)((i * amplitude) / (SAMPLES / 2));
            square_val[i] = (uint16_t)amplitude;
        } else {
            tri_val[i] = (uint16_t)(amplitude - ((i - SAMPLES / 2) * amplitude) / (SAMPLES / 2));
            square_val[i] = 0;
        }
    }
}




/**
  * @brief  Timer ARR değerini hedef frekansa göre ayarlar.
  */
void Set_Wave_Frequency(uint32_t target_hz) {
    if (target_hz == 0) return;
    current_frequency = target_hz; // Durumu guncelle

    uint32_t timer_clock = 64000000;
    uint32_t psc_val = 400;
    uint32_t new_period = timer_clock / (psc_val * SAMPLES * target_hz);

    if (new_period < 1) new_period = 1;
    if (new_period > 65535) new_period = 65535;

    __HAL_TIM_SET_AUTORELOAD(&htim6, (new_period - 1));
}




/**
  * @brief  DAC ve DMA'yı mevcut ayarlara göre yeniden başlatır.
  */
void Update_Wave_Output(void) {
    // 1. DMA'yı durdur
    HAL_DAC_Stop_DMA(&hdac1, DAC_CHANNEL_1);

    // 2. Hangi dizi gönderilecek seç
    uint16_t *pWave;
    switch (current_wave_type) {
        case 0:  pWave = sine_val;   break;
        case 1:  pWave = cos_val;    break;
        case 2:  pWave = tri_val;    break;
        case 3:  pWave = square_val; break;
        default: pWave = sine_val;   break;
    }

    // 3. DMA'yı tekrar başlat
    HAL_DAC_Start_DMA(&hdac1, DAC_CHANNEL_1, (uint32_t*)pWave, SAMPLES, DAC_ALIGN_12B_R);
}
